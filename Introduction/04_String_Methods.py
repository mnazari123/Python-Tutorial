name = "N12"

print(len(name))        # Print the length of the string
print(name.find("o"))   # prints the first parameter in the string
print(name.capitalize())# capitalizes all the String
print(name.upper())     # changes each letter to capital letter
print(name.lower())     # changes each letter to small letter
print(name.isdigit())   # checks if the string we check is degit
print(name.isalpha())   # checks the paramter if it is string
print(name.count("o"))  # index of the parameter in the string
print(name.replace("o", "a")) # replaces the fist parameter with the second in the String
print(name * 3)         # prints the string 3 times.